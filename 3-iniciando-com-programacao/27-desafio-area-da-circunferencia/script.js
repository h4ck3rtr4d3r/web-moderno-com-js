//Calcule a área da circunferência
// Fórmula para o calculo de uma circuferência: PI * raio *raio

const PI = Math.PI;
let raio = 10;
const areaCirc = PI * (raio * raio);

console.log(
  `O valor do calculo da área da circunferência, dado que PI é ${PI} e raio ${raio}, é igual a ${areaCirc} m2.`
);
